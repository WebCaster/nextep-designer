package com.nextep.datadesigner.sqlgen.gui.jface;

import org.eclipse.jface.viewers.ILabelProviderListener;
import org.eclipse.jface.viewers.ITableLabelProvider;
import org.eclipse.swt.graphics.Image;
import com.nextep.datadesigner.sqlgen.model.ISynchronizationFilter;
import com.nextep.designer.sqlgen.ui.SQLGenImages;

public class SynchronizationFilterLabelProvider implements ITableLabelProvider {

	@Override
	public Image getColumnImage(Object element, int columnIndex) {
		if(columnIndex==0) {
			return SQLGenImages.ICON_FILTER;
		}
		return null;
	}

	@Override
	public String getColumnText(Object element, int columnIndex) {
		ISynchronizationFilter filter = (ISynchronizationFilter)element;
		switch(columnIndex) {
		case 0:
			return filter.getName();
		case 1:
			return filter.getType().getName();
		}
		return null;
	}

	@Override
	public void addListener(ILabelProviderListener listener) {
		// TODO Auto-generated method stub

	}

	@Override
	public void dispose() {
		// TODO Auto-generated method stub

	}

	@Override
	public boolean isLabelProperty(Object element, String property) {
		// TODO Auto-generated method stub
		return false;
	}

	@Override
	public void removeListener(ILabelProviderListener listener) {
		// TODO Auto-generated method stub

	}

}
