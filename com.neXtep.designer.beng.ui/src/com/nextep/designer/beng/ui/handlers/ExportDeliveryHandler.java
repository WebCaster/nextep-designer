package com.nextep.designer.beng.ui.handlers;

import org.eclipse.core.commands.AbstractHandler;
import org.eclipse.core.commands.ExecutionEvent;
import org.eclipse.core.commands.ExecutionException;
import org.eclipse.jface.viewers.ISelection;
import org.eclipse.jface.viewers.IStructuredSelection;
import org.eclipse.ui.handlers.HandlerUtil;
import com.nextep.designer.beng.model.IDeliveryModule;
import com.nextep.designer.beng.ui.BengUIPlugin;
import com.nextep.designer.beng.ui.services.IDeliveryExportUIService;

/**
 * @author Christophe Fondacci
 */
public class ExportDeliveryHandler extends AbstractHandler {

	/**
	 * @see org.eclipse.core.commands.AbstractHandler#execute(org.eclipse.core.commands.ExecutionEvent)
	 */
	@Override
	public Object execute(ExecutionEvent event) throws ExecutionException {
		ISelection s = HandlerUtil.getCurrentSelection(event);
		if (s instanceof IStructuredSelection) {
			IDeliveryModule module = (IDeliveryModule) ((IStructuredSelection) s).getFirstElement();
			BengUIPlugin.getService(IDeliveryExportUIService.class).exportDelivery(module);
		}

		return null;
	}

}
