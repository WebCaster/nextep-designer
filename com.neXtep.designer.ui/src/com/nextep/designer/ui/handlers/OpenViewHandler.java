package com.nextep.designer.ui.handlers;

import org.eclipse.core.commands.AbstractHandler;
import org.eclipse.core.commands.ExecutionEvent;
import org.eclipse.core.commands.ExecutionException;
import org.eclipse.ui.IWorkbenchWindow;
import org.eclipse.ui.PartInitException;
import org.eclipse.ui.handlers.HandlerUtil;
import com.nextep.datadesigner.exception.ErrorException;

/**
 * A handler for open view commands.
 *
 * @author Christophe Fondacci
 *
 */
public class OpenViewHandler extends AbstractHandler {

	/**
	 * @see org.eclipse.core.commands.AbstractHandler#execute(org.eclipse.core.commands.ExecutionEvent)
	 */
	@Override
	public Object execute(ExecutionEvent event) throws ExecutionException {
		IWorkbenchWindow window = HandlerUtil.getActiveWorkbenchWindowChecked(event);

		String viewId = event.getParameter("com.neXtep.designer.ui.viewId");
		if(viewId == null || "".equals(viewId)) {
			throw new ErrorException("No command viewId provided, cannot open the view.");
		} else {
			try {
				window.getActivePage().showView(viewId);
			} catch( PartInitException e ) {
				throw new ErrorException(e);
			}
		}
		// TODO Auto-generated method stub
		return null;
	}

}
