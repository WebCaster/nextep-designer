package com.nextep.datadesigner.gui.impl;

import java.util.ArrayList;
import java.util.Collection;
import org.eclipse.jface.viewers.ISelection;
import org.eclipse.jface.viewers.ISelectionChangedListener;
import org.eclipse.jface.viewers.ISelectionProvider;
import org.eclipse.jface.viewers.SelectionChangedEvent;
import org.eclipse.jface.viewers.StructuredSelection;

/**
 * A generic selection provider to use for delegation. It handles the selection change listeners
 * notification. 
 * @author Christophe Fondacci
 *
 */
public class GenericSelectionProvider implements ISelectionProvider {

	private Collection<ISelectionChangedListener> listeners;
	private ISelection currentSelection;

	public GenericSelectionProvider() {
		listeners = new ArrayList<ISelectionChangedListener>();
		currentSelection = new StructuredSelection();
	}

	@Override
	public void addSelectionChangedListener(ISelectionChangedListener listener) {
		listeners.add(listener);
	}

	@Override
	public ISelection getSelection() {
		return currentSelection;
	}

	@Override
	public void removeSelectionChangedListener(ISelectionChangedListener listener) {
		listeners.remove(listener);
	}

	@Override
	public void setSelection(ISelection selection) {
		this.currentSelection = selection;
		for (ISelectionChangedListener l : listeners) {
			l.selectionChanged(new SelectionChangedEvent(this, currentSelection));
		}
	}

}
